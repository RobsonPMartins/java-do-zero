package arrays;

public class arrays {
    
    public static void main(String[] args) {
        // Atribuição e inicialização do array
        int[] numeros = new int[5];

        //Atribuição dos valores aos elementos
        numeros[0] = 10;
        numeros[1] = 20;
        numeros[2] = 30;
        numeros[3] = 40;
        numeros[4] = 50;

        // acessando elemento do array e imprimindo-os
        System.out.println("Elemento 0: " + numeros[0]);
        System.out.println("Elemento 3: " + numeros[3]);
    }
}
